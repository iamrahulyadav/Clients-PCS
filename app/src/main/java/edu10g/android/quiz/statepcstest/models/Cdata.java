public class Cdata {
        private String id;
        private String name;
        private String banner;

        public Cdata()
        {

        }


        // Getter and Setter Method
        public String getId()
        {
                return id;
        }

        public void setId(String id)
        {
                this.id = id;
        }

        public String getName()
        {
                return name;
        }

        public void setName(String name)
        {
                this.name = name;
        }
        public String getBanner()
        {
                return banner;
        }

        public void setBanner(String banner)
        {
                this.banner = banner;
        }
}
